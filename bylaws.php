<div id="bylaws">
<h2>Bylaws</h2> 

<h3>Article I. Name and Purpose</h3>
<h4 class="sec1">Section 1. Name</h4>
<p>The name of this club is MEGA. The club name stands for “Makers of Engaging Games and Animation”.</p>  

<h4>Section 2. Purposes</h4>
<p>This club shall...</p>
<ol>
    <li>Provide opportunities to study the art, design, and development of games, animation, and cinema.</li>
    <li>Sponsor multiple community-building events within the School of Digital Media each semester.</li>
    <li>Within the School of Digital Media, the College of Education and Human Resources, and Ferris State University, work to establish a culture that embraces diversity and respect.</li>
    <li>Facilitate and moderate a virtual community online for School of Digital Media students.</li>
</ol>


<h3>Article II. Membership</h3>
<p>Active membership in the club shall be open to any student of Ferris State University. </p>

<h3>Article III. Board of Directors</h3>
<p>The affairs of the club shall be administered by a Board of Directors. The Board of Directors is comprised of officers, including a Faculty Liaison, President, Vice President(s), Secretary, Treasurer, and Director of Communications.</p>
    
<h3>Article IV. Amendments</h3>
<p>Amendments to these Bylaws may be proposed in writing by the Board of Directors or any members of the club and shall be submitted to a vote of the Board of Directors. Two weeks from date of submittal to members shall be allowed for consideration before voting at the next meeting. The majority of all votes shall be held decisive.</p>
    
<h3>Article V. Meetings</h3>
<h4 class="sec1">Section 1. Club Meetings</h4>
<p>Meetings shall be held in accordance with the number set by the officers and directors.</p>
<h4>Section 2. Election Meetings</h4>
<p>Once each semester (Spring, Fall, and Summer) the club will hold a meeting to nominate and vote for officers (see Article IX).
</p>
<h4>Section 3. Board Meetings</h4>
<p>Each member of the board of directors in good standing shall be entitled to one vote upon any matter coming before the club.</p>
    
<h3>Article VI. Dues</h3>
<p>Membership in the club does NOT require any payment of dues.</p>
    
<h3>Article VII. Governing Body</h3>
<h4 class="sec1">Section 1. Board of Directors</h4>
<p>The governing body of the organization shall be a Board of Directors consisting of not less than five (5) nor more than eight (8) officers.
<br><br>
The Board of Directors shall be the policy-making body of the organization and continuously observe activities of the club to ensure adherence to approved policies. It is responsible for setting immediate goals for the current administrative semester and long-range goals for future club achievement.
<br><br>
The Board shall have access to the books, records, and mailing list of the club. The Board at its discretion may request a financial statement from the Treasurer and may request an accounting from any officer and shall have power to approve or reject the actions or recommendations of any officer or committee.
</p>    
    
<h4>Section 2. Officers</h4>
<p>The officers shall be the administrative body of the club and shall consist of a faculty liaison, president, vice president(s), secretary, treasurer, and director of communications. All officers, except for the faculty liaison, shall be elected as provided in the Bylaws.
<br><br>
The officers shall maintain their roles and responsibilities until their position is filled at the next election.</p>
    
<h4>Section 3. Faculty Liaison</h4>
<p>The faculty liaison shall function as the Chair of the Board and shall preside at all board meetings and perform the usual duties incumbent upon such a position.</p>
    
<h4>Section 4. President</h4>
<p>The president shall preside at all meetings of the club and shall be a member of all local club committees. The president oversees communication with members, the board of directors, and faculty advisors.</p>

<h4>Section 5. Vice President(s)</h4>
<p>The vice president shall support and assist the efforts of the president. This includes attending functions with the president where appropriate and representing the club in cases where the president is unable to attend. In the temporary absence of the president, the vice president shall be designated by the president to exercise and perform his/her duties. At other times the vice president shall carry out such duties as may be delegated by the president or the board of directors.</p>
    
<h4>Section 6. Secretary</h4>
<p>The secretary shall keep the minutes of the club meetings and be its custodian of the records of the club, conduct correspondence, have charge of the mailing list, and report to the faculty liaison on the results of the meetings. The secretary shall notify all members of club meetings and shall keep available for inspection by the club members copies of the Bylaws.
<br><br>
The secretary shall be responsible for creating, storing, organizing, and making accessible all documents for the club’s online document repository(ies).</p>
    
<h4>Section 7. Treasurer</h4>
<p>The treasurer shall be authorized to collect all monies payable to the club, shall be charged with keeping the funds of the club, and from such funds shall make the necessary disbursements. The treasurer shall keep the club’s financial accounts and shall enter in detail all receipts and disbursements. The treasurer shall be authorized to open an account in the name of the club and deposit all funds therein. The treasurer shall also help develop and implement fundraising efforts for the club.</p>
    
<h4>Section 8. Director of Communications</h4>
<p>The director of communications maintains and expands the organization’s presence on various social media and communication platforms. This person assists the president with promotional and communication efforts as needed, and he/she tries to get members and the public more involved with the club and its events.</p>
    
<h4>Section 9. Other Standing Officers</h4>
<p>Standing officers, if necessary, shall be appointed by the president and shall carry out the details necessary to the office to which appointed.</p>
    
<h3>Article VIII. Elections</h3>
<h4 class="sec1">Section 1. Frequency</h4>
<p>Once each semester (Spring, Fall, and Summer), the faculty liaison shall schedule a public election to fill each of the officer positions, except the faculty liaison. The meeting shall be public and open to all club members.</p>
<h4>Section 2. Proceedings</h4>
<p>The faculty liaison will chair the election meeting. During an election meeting, club members shall nominate their peers for the various officer positions. For a nomination to be valid, it must be seconded. Members may nominate (or second) themselves for a position.
<br><br>
Each nominee will have the opportunity to briefly speak about their qualifications and goals. The club members will vote for each officer position.
<br><br>
Each position will be decided with a simple majority. In the event of a tie, the faculty liaison shall cast the deciding vote.
</p>

<h3>Article IX. Committees</h3>
<p>To delegate and accomplish its work, the club shall use multiple committees.</p>
<h4>Section 1. Formation</h4>
<p>The president or executive board shall form committees as necessary. The president or executive board shall appoint a club member to chair the committee.</p>

<h4>Section 2. Chair</h4>
<p>The chair of a committee may be any club member. The chairperson is accountable for scheduling committee meetings, delegating committee work, and reporting back to the president or board of directors.</p>
    
<h4>Section 3. Membership</h4>
<p>Any club member may participate on any committee.</p>
    
<h4>Section 4. Limitations</h4>
<p>No committee shall take any action committing the club without the express authorization of the president. Action by any committee shall be upon affirmative vote of a majority of its members.<br><br></p>
    
    
    
